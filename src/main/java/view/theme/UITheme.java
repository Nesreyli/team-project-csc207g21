package view.theme;

import java.awt.Color;

/**
 * Defines the theme colors and font used across the application.
 */
public final class UITheme {

    /** Background color for main panels */
    public static final Color BG = new Color(240, 242, 245);

    /** Background color for cards or secondary panels */
    public static final Color CARD_BG = Color.WHITE;

    /** Primary accent color for buttons and highlights */
    public static final Color PRIMARY = new Color(0, 123, 255);

    /** Dark text color */
    public static final Color TEXT_DARK = new Color(51, 51, 51);

    /** Default font name for the application */
    public static final String FONT_NAME = "Segoe UI";
}
